DROP TABLE LECTURE;
DROP TABLE ENROLL;
DROP TABLE COURSE;
DROP TABLE STUDENT;
DROP TABLE PROFESSOR;

CREATE TABLE PROFESSOR(
    NO NUMBER PRIMARY KEY,
    NAME VARCHAR2(20),
    MAJOR VARCHAR2(20)
);
CREATE TABLE STUDENT(
    NO NUMBER DEFAULT 200001 PRIMARY KEY,
    NAME VARCHAR2(20),
    ADDRESS VARCHAR2(200),
    GRADE NUMBER CHECK(GRADE IN(1,2,3,4)),
    PROFESSOR_NO NUMBER REFERENCES PROFESSOR(NO)
);
CREATE TABLE COURSE(
    NO NUMBER PRIMARY KEY,
    NAME VARCHAR2(20),
    SCORE NUMBER(3, 1)
    );
CREATE TABLE ENROLL(
    NO NUMBER PRIMARY KEY,
    STUDENT_NO NUMBER REFERENCES STUDENT(NO),
    COURSE_NO NUMBER REFERENCES COURSE(NO),
    CREATE_AT DATE
);
CREATE TABLE LECTURE(
    NO NUMBER PRIMARY KEY,
    PROFESSOR_NO NUMBER REFERENCES PROFESSOR(NO),
    ENROLL_NO NUMBER REFERENCES ENROLL(NO),
    NAME VARCHAR2(20),
    CLASS VARCHAR2(10)
);

